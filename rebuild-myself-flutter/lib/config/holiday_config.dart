/// 2026 年中国大陆法定节假日及调休安排
/// 数据来源：国务院办公厅通知
class HolidayConfig {
  HolidayConfig._();

  /// 法定节假日（不用上班的日期）
  static const _holidays = <String>{
    // 元旦 2026
    '2026-01-01', '2026-01-02', '2026-01-03',
    // 春节 2026-02-17 ~ 2026-02-23 (除夕~初六)
    '2026-02-17', '2026-02-18', '2026-02-19', '2026-02-20',
    '2026-02-21', '2026-02-22', '2026-02-23',
    // 清明节 2026-04-05 ~ 2026-04-07
    '2026-04-05', '2026-04-06', '2026-04-07',
    // 劳动节 2026-05-01 ~ 2026-05-05
    '2026-05-01', '2026-05-02', '2026-05-03', '2026-05-04', '2026-05-05',
    // 端午节 2026-06-19 ~ 2026-06-21
    '2026-06-19', '2026-06-20', '2026-06-21',
    // 中秋节 2026-09-25 ~ 2026-09-27
    '2026-09-25', '2026-09-26', '2026-09-27',
    // 国庆节 2026-10-01 ~ 2026-10-07
    '2026-10-01', '2026-10-02', '2026-10-03', '2026-10-04',
    '2026-10-05', '2026-10-06', '2026-10-07',
  };

  /// 调休补班日（周末但要上班）
  static const _makeupWorkdays = <String>{
    '2026-01-04', // 元旦补班
    '2026-02-15', '2026-02-28', // 春节前后补班
    '2026-04-11', // 清明补班
    '2026-05-10', // 劳动节补班
    '2026-06-14', // 端午节补班
    '2026-09-20', // 中秋节补班
    '2026-09-19', '2026-10-10', '2026-10-11', // 国庆节前后补班
  };

  /// 判断指定日期是否为工作日
  static bool isWorkday(DateTime date) {
    final ds =
        '${date.year}-${date.month.toString().padLeft(2, '0')}-${date.day.toString().padLeft(2, '0')}';

    if (_makeupWorkdays.contains(ds)) return true;
    if (_holidays.contains(ds)) return false;

    // 默认：周一到周五为工作日
    final w = date.weekday;
    return w >= 1 && w <= 5; // Monday=1, Friday=5
  }

  /// 判断指定日期是否为周末或节假日
  static bool isRestDay(DateTime date) => !isWorkday(date);
}
